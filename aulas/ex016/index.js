// Estrutura de Repetição com condição no final
var c = 1
do{
    console.log(`Passo ${c}`)
    c++
}while(c <= 6)


// Estrutura de Repetição com condição no começo
var c = 1
while(c <= 6){
    console.log(`Passo ${c}`)
    c++
}

//Estrutura de Repetição com variavel de controle
for(var c=1; c <=10; c++){
    console.log(`Passo ${c}`)
}

// Sem estrutura de repetição
