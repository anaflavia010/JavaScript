var num = [5, 8, 2, 9, 3]
console.log(`Nosso vetor é o: ${num}`)
console.log(`O tamanho do vetor é: ${num.length} elementos`)
num.push(1)
console.log(`Adicionei 1 ao final do vetor: ${num}`)
console.log(`O primeiro elemento do vetor é: ${num[0]}`)
console.log(`O último elemento do vetor é: ${num[5]}`)
console.log(`O vetor ordenado em ordem crescente é: ${num.sort()}`)
console.log(`O vetor ordenando em ordem descrecente é: ${num.sort().reverse()}`)